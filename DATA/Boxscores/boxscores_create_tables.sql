CREATE TABLE IF NOT EXISTS "Boxscores"(
"GAME_ID" INTEGER, "TEAM_ID" INTEGER, "PLAYER_ID" INTEGER, 
 "FGM" INTEGER, "FGA" INTEGER, 
 "FG3M" INTEGER, "FG3A" INTEGER,
 "FTM" INTEGER, "FTA" INTEGER,
 PRIMARY KEY ("GAME_ID", "TEAM_ID", "PLAYER_ID"),
 FOREIGN KEY ("TEAM_ID") REFERENCES "Teams"("TEAM_ID"),
 FOREIGN KEY ("PLAYER_ID") REFERENCES "Players"("PLAYER_ID")
 );
CREATE TABLE IF NOT EXISTS "Teams"(
"TEAM_ID" INTEGER, "TEAM_NAME" TEXT,
PRIMARY KEY ("TEAM_ID")
);
CREATE TABLE IF NOT EXISTS "Players"(
"PLAYER_ID" INTEGER, "PLAYER_NAME" TEXT,
PRIMARY KEY ("PLAYER_ID")
);
CREATE TABLE IF NOT EXISTS "Season2025" (
  "TEAM_ID" INTEGER,
  "FG_PCT_AVG" REAL,
  "FG3_PCT_AVG" REAL,
  "FT_PCT_AVG" REAL,
  "SEASON_WINS" REAL,
  PRIMARY KEY ("TEAM_ID"),
  FOREIGN KEY ("TEAM_ID") REFERENCES "Teams"("TEAM_ID")
);



